// Parse helper
function parseWords(raw) {
  return raw
    .split("\n")
    .filter(line => line.trim() !== "")
    .map(line => {
      const [hanzi, pinyin, meaning] = line.split(",");
      return { hanzi, pinyin, meaning };
    });
}

